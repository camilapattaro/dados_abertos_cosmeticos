--1.Count thr distinct number of products.
SELECT 
  COUNT(NO_PRODUTO) 
FROM 
  DADOS_ABERTOS_COSMETICO
  
--71.074 products.

--2.Searching the products related to ACNE.

SELECT 
  COUNT(NO_PRODUTO)
FROM 
  DADOS_ABERTOS_COSMETICO
WHERE 
   NO_PRODUTO LIKE '%ACNE%'
OR NO_PRODUTO LIKE '%ESPINHA%'

--736 products, which is less than 1%.

--3.Searching adesivos para acne.

SELECT 
  NO_PRODUTO
FROM 
  DADOS_ABERTOS_COSMETICO 
WHERE 
  NO_PRODUTO LIKE '%ADESIVO%'

--One product was found: O BOTICARIO ADESIVOS SECATIVOS PARA ACNE.

--4.Analysing the companies producing most of the acne products.

SELECT 
NO_RAZAO_SOCIAL_EMPRESA,
COUNT(DISTINCT NO_PRODUTO)
FROM
(SELECT 
 NO_RAZAO_SOCIAL_EMPRESA,
 NO_PRODUTO
FROM 
  DADOS_ABERTOS_COSMETICO1 
WHERE
   NO_PRODUTO LIKE '%ACNE%'
OR NO_PRODUTO LIKE '%ESPINHA%'
OR NO_PRODUTO LIKE '%ADESIVO')
GROUP BY 
NO_RAZAO_SOCIAL_EMPRESA

--172 companies total.

